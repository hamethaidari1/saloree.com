/** Translate a key to the given language, falling back to English. */
export function t(key: TranslationKey, lang: LangCode = "en"): string {
  const dict = translations[lang];
  if (dict && dict[key] !== undefined) {
    return dict[key];
  }

  // English fallback + console warning
  if (lang !== "en") {
    console.warn(`[i18n] Missing key "${key}" for language "${lang}". Falling back to English.`);
  }
  return translations.en[key] ?? String(key);
}

